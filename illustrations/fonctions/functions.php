<?php

/**
 * @param $nombre c'est un entier
 * @return resultat qui est le nombre plus 2
 */
function ajouter2(int $nombre): int {
    $nombre = $nombre + 2;
    return $nombre;
}

/**
 * @param $nombre c'est un entier
 * @return resultat qui est le nombre plus 3
 */
function ajouter3(int $nombre): int {
    $nombre = $nombre + 3;
    return $nombre;
} 

?>